#include "libft.h"

void	ft_putnbr(int n)
{
	int	i;

	if (n < -2147483647 || n > 2147483647)
	{
		if (n == -2147483648)
			ft_putstr("-2147483648");
		return ;
	}
	i = 1;
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	while (n / i > 9)
		i *= 10;
	while (i > 0)
	{
		ft_putchar(n / i + '0');
		n %= i;
		i /= 10;
	}
}
