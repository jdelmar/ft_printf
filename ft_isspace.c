#include "libft.h"

int		ft_isspace(int c)
{
	unsigned char uc;

	uc = (unsigned char)c;
	return (uc == '\t' || uc == '\n' || uc == '\r' || uc == ' '	\
	|| uc == '\v' || uc == '\f' ? 1 : 0);
}
