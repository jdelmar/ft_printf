#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	i;

	if (n < -2147483647 || n > 2147483647)
	{
		if (n == -2147483648)
			ft_putstr_fd("-2147483648", fd);
		return ;
	}
	i = 1;
	if (n < 0)
		ft_putchar_fd('-', fd);
	n = (n < 0) ? -n : n;
	while (n / i > 9)
		i *= 10;
	while (i > 0)
	{
		ft_putchar_fd(n / i + '0', fd);
		n %= i;
		i /= 10;
	}
}
